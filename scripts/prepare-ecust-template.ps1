    "Abstract abstract abstract." = "Write the English abstract here.";
    "Keywords \ \ }Deep learning; xxx; xxx; xxx" = "Keywords \ \ }keyword one; keyword two; keyword three";
    "绪论绪论绪论绪论绪论绪论绪论绪论绪论绪论绪论绪论绪论绪论绪论绪论绪论。" = "本章介绍研究背景、研究意义、国内外研究现状、研究内容与方法以及论文结构。";
    "理论基础理论基础理论基础理论基础理论基础理\cite{lee2021mlcourse}论基础理论基础理论基础理论基础理论基础理论基础理论基础理论基础理论基础理论基础理论基础理论基础理论基础理论基础。" = "本章介绍研究所需的理论基础、核心概念和相关方法。";
    "XXX模型" = "研究方法与模型";
    "模型训练和改进" = "方法设计与实现";
    "此篇论文完稿之际，首先要感谢致谢致谢致谢致谢致谢致谢致谢致谢致谢致谢致谢致谢致谢致谢致谢致谢致谢致谢致谢致谢致谢致谢致谢致谢致谢致谢致谢致谢致谢致谢致谢致谢致谢。" = "这里填写致谢内容。";
