foreach ($key in $replacements.Keys) {
    $content = $content.Replace($key, $replacements[$key])
}

Set-Content -LiteralPath $tex -Value $content -Encoding UTF8

$notice = @"
# ECUST Template Source

This directory is generated by `scripts/prepare-ecust-template.ps1`.

