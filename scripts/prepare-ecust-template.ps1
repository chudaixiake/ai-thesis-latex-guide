param(
    [string]$Destination = "template/ecust-master-thesis",
    [string]$Source = "C:\Users\xiaoy\Desktop\ecust-master-thesis-latex-main",
    [string]$Repo = "",
    [switch]$Force
)

$ErrorActionPreference = "Stop"

$root = Resolve-Path -LiteralPath (Join-Path $PSScriptRoot "..")
if ([System.IO.Path]::IsPathRooted($Destination)) {
    $destPath = [System.IO.Path]::GetFullPath($Destination)
}
else {
    $destPath = [System.IO.Path]::GetFullPath((Join-Path $root $Destination))
}

if ((Test-Path -LiteralPath $destPath) -and -not $Force) {
    throw "Destination already exists: $destPath. Use -Force to replace it."
}

if (Test-Path -LiteralPath $destPath) {
    $resolved = Resolve-Path -LiteralPath $destPath
    $leaf = Split-Path -Leaf $resolved.Path
    $parent = Split-Path -Parent $resolved.Path
    $driveRoot = [System.IO.Path]::GetPathRoot($resolved.Path)
    $safeTemplateNames = @("template", "ecust-master-thesis")
    if (($resolved.Path -eq $driveRoot) -or [string]::IsNullOrWhiteSpace($parent) -or ($safeTemplateNames -notcontains $leaf)) {
        throw "Refusing to remove non-template path: $($resolved.Path)"
    }
    Remove-Item -LiteralPath $resolved.Path -Recurse -Force
}

$destParent = Split-Path -Parent $destPath
$destRoot = [System.IO.Path]::GetPathRoot($destPath)
if ((-not [string]::IsNullOrWhiteSpace($destParent)) -and ($destParent -ne $destRoot)) {
    New-Item -ItemType Directory -Force -Path $destParent | Out-Null
}
New-Item -ItemType Directory -Force -Path $destPath | Out-Null

if ($Source -and (Test-Path -LiteralPath $Source)) {
    Copy-Item -Path (Join-Path $Source "*") -Destination $destPath -Recurse -Force
    $sourceDescription = "Local standard template: $Source"
}
elseif ($Repo) {
    git clone --depth 1 $Repo $destPath
    $gitDir = Join-Path $destPath ".git"
    if (Test-Path -LiteralPath $gitDir) {
        Remove-Item -LiteralPath $gitDir -Recurse -Force
    }
    $sourceDescription = "Remote template source: $Repo"
}
else {
    throw "Cannot find local template source: $Source. Provide -Source or -Repo."
}

$tex = Join-Path $destPath "template.tex"
if (-not (Test-Path -LiteralPath $tex)) {
    throw "Cannot find template.tex in $destPath"
}

$content = Get-Content -LiteralPath $tex -Raw -Encoding UTF8

$replacements = [ordered]@{
    "论文题目" = "你的中文论文题目";
    "Your Thesis Title in English" = "Your English Thesis Title";
